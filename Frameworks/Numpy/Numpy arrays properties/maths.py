import numpy as np 
array = np.array([1, 2, 3, 4, 5])
print("Original array:", array)
# Addition
added_array = array + 10    
print("Array after addition:", added_array)

# Subtraction
subtracted_array = array - 2        
print("Array after subtraction:", subtracted_array)
# Multiplication
multiplied_array = array * 3        
print("Array after multiplication:", multiplied_array)
# Division
divided_array = array / 2
print("Array after division:", divided_array)

print("Array after squaring:", array ** 2)