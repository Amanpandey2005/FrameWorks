import numpy as np;
arr = np.array([1, 2, 3]);
print("Sum:", np.sum(arr))  # Output: 6
print("Mean:", np.mean(arr))  # Output: 2.0
print("Standard Deviation:", np.std(arr))  # Output: 0.816496580927726
print("Minimum:", np.min(arr))  # Output: 1
print("Maximum:", np.max(arr))  # Output: 3
print("Median:", np.median(arr))  # Output: 2.0
print("Product:", np.prod(arr))  # Output: 6
print("Cumulative Sum:", np.cumsum(arr))  # Output: [1 3 6]
print("Cumulative Product:", np.cumprod(arr))  # Output: [1 2 6]